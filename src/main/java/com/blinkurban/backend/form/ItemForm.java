package com.blinkurban.backend.form;

public class ItemForm{
	private String name;
	private String description;
	private String gender;
	
	public ItemForm(){}
	
	public String getName() {
		return name;
	}
	public String getDescription() {
		return description;
	}
	public String getGender() {
		return gender;
	}
	
}