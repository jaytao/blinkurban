package com.blinkurban.backend.form;

public class ModelForm{
	private String name;
	private int height;
	private int weight;
	private String description;
	
	public ModelForm(){}

	public String getName() {
		return name;
	}

	public int getHeight() {
		return height;
	}

	public int getWeight() {
		return weight;
	}

	public String getDescription() {
		return description;
	}
	
	
}